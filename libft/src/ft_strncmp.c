#include "../include/libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while ((s1[i] || s2[i]) && i < n)
	{
		if (s1[i] != s2[i])
		{
			if (!(s1[i] >= 0 && s1[i] <= 127) || !(s2[i] >= 0 && s2[i] <= 127))
				return (1);
			return (s1[i] - s2[i]);
		}
		i++;
	}
	return (0);
}
